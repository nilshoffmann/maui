package net.sf.maltcms.chromaui.annotations;

import java.awt.Rectangle;
import java.awt.Shape;
import java.awt.geom.AffineTransform;
import java.awt.geom.PathIterator;
import java.awt.geom.Point2D;
import java.awt.geom.Rectangle2D;

/**
 *
 * @author Nils Hoffmann
 */
public class VisualPeakAnnotation implements Shape {

    private final Point2D.Double center;
    private final Shape shape;
    private final PeakAnnotationType peakAnnotationType;

    public VisualPeakAnnotation(Shape s, Point2D.Double center, PeakAnnotationType peakAnnotationType) {
        this.shape = s;
        this.center = center;
        this.peakAnnotationType = peakAnnotationType;
    }

    public Point2D getCenter() {
        return this.center;
    }

    public PeakAnnotationType getPeakAnnotationType() {
        return peakAnnotationType;
    }

    @Override
    public Rectangle getBounds() {
        return shape.getBounds();
    }

    @Override
    public Rectangle2D getBounds2D() {
        return shape.getBounds2D();
    }

    @Override
    public boolean contains(double x, double y) {
        return shape.contains(x, y);
    }

    @Override
    public boolean contains(Point2D p) {
        return shape.contains(p);
    }

    @Override
    public boolean intersects(double x, double y, double w, double h) {
        return shape.intersects(x, y, w, h);
    }

    @Override
    public boolean intersects(Rectangle2D r) {
        return shape.intersects(r);
    }

    @Override
    public boolean contains(double x, double y, double w, double h) {
        return shape.contains(x, y, w, h);
    }

    @Override
    public boolean contains(Rectangle2D r) {
        return shape.contains(r);
    }

    @Override
    public PathIterator getPathIterator(AffineTransform at) {
        return shape.getPathIterator(at);
    }

    @Override
    public PathIterator getPathIterator(AffineTransform at, double flatness) {
        return shape.getPathIterator(at, flatness);
    }
}
